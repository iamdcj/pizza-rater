import type { NextPage } from "next";
import Head from "next/head";
import Image from "next/image";
import styles from "../styles/Home.module.css";

interface Rating {
  id: string;
  name: string;
  location: string;
  slice: string;
  rating: number;
}

interface Home {
  ratings: Rating[];
}

const Home: NextPage<Home> = ({ ratings = [] }) => {
  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>üóΩüçï Rate Your Slice üçïüóΩ </h1>

        <p className={styles.description}>
          My personal ratings for all the slices I&apos;ve tried in NYC.
        </p>

        <h3 className={styles.description}>Current Leaderboard</h3>
        {ratings?.length > 0 && (
          <ul>
            {ratings.map(({ id, name, location, slice, rating }, i) => (
              <li key={id}>
                üçï {name}, {location} - {slice} Slice - {rating}
              </li>
            ))}
          </ul>
        )}
      </main>
    </div>
  );
};

export async function getServerSideProps() {
  let ratings: any[] | string = [];

  try {
    const ratingsData = await fetch("http://localhost:3000/api/ratings");

    if (!ratingsData.ok) {
      throw new Error("Unable to fetch ratings");
    }

    ratings = await ratingsData.json();

    if (ratings.length < 1) {
      throw new Error("Fallback to dummy data");
    }
  } catch (error) {
    console.error(error.message);

    ratings = [
      {
        id: 1,
        name: "L'Industrie Pizzeria",
        location: "Brooklyn",
        slice: "Fig Jam & Bacon",
        rating: 8.5,
      },
      {
        id: 2,
        name: "Paulie Gee's",
        location: "Madison Square Garden",
        slice: "Cheese",
        rating: 8,
      },
    ];
  }

  return {
    props: { ratings },
  };
}

export default Home;
